	<section class="event-feature sec-padding"  id="What_Is_Credit_Union">
		<div class="container">
			<div class="row">
				<div class="sec-title colored text-center">
					<br>
				<h3><b>What Is Credit Union</b></h3>
				<span class="decor"><span class="inner"></span></span>
				
			</div>
	          <div class="col-sm-12">
	                <div class="causes-details clearfix">
	                    
	                    
	                    <p>A Credit Union is a co-operative financial institution, that is owned and controlled by its members and operated for the purpose of promoting thrift, providing credit at reasonable rates, and providing other financial services to its members.</p>
	                    <p>In many countries, the financial industry is dominated by commercial or government-controlled banks that help implement monetary policies set by a government agency. Credit Unions are different in that they serve the needs of individuals. Most private sector financial institutions are controlled by investors in making a profit on the services offered to user/customers. In Credit Unions, the user/customer is also the owner. All profits made on services offered belong to the membership. Member ownership contributes to the ability to operate soundly while charging affordable rates and serving people other organizations consider unprofitable.</p>
	                    <p>A Savings and Credit Cooperative (SCC) is a group of people who join together to pool their savings and make loans to each other at reasonable rates of interest that cover all costs and provide for adequate sized reserves. The group also aims to educate its members on the wise use of money so they can improve their lives.</p>
	                    <p>To make the process easier, the group maintains a business structure – a cooperative – which functions as an intermediary between savers and borrowers. The members of the group own and control the organization.</p>
	                   
	                   <div class="sec-title"><h4><b>WHO CAN JOIN?</b></h4></div>
	                    <p>SCCs are organized to serve one or more groups of people who have something in common. Everyone who shares the common bond qualifies to join the SCC. They are part of its field of membership. To become a member, a person must purchase one ownership share. Each SCC should be independent and financially self sustainable with an obligation to put the best interests of its members over any other concerns.</p>
						<div class="sec-title"><h4><b>FINANCIAL MECHANICS</b></h4></div>
	                    <p>A SCC uses its member’s shares and deposits to fund loans. So it pays savers for the use of their money. This payment is both a fair return for that use and an incentive to save more. As the pool of savings grows, more loans can be made, more income can be generated, and more members can be served.</p>
	                    <p>The people who borrow from the pool pay interest for the use of the money. This interest is the SCC’s main source of income. Total income must cover the return paid to savers, the SCC’s operating expenses, and reserves for financial stability. It may also fund education programs and additional financial services. Most SCCs identify the benefits they want to offer, then set about earning the income needed to fund them.</p>
	                    <p>SCCs are frequently considered as “non-profit” organizations. By definition these organizations keep only enough income to cover current operating expenses. For SCCs this strategy would amount to self-destruction. It doesn’t allow for expansion or additional benefits when the membership grows and changes. And it doesn’t allow for the losses that inevitably occur, but which are difficult to predict in advance.</p>
	                    <p>What makes a SCC non-profit is that all surplus funds are returned to the members in one way or another. Most often the “return” is in the form of benefits such as lower loan rates, additional services, larger provisions against loss, or new equipment that permits better service. After a SCC has met its other goals, it may give any remaining surplus back to the members as share dividends.</p>
	                    <p>A SCC is also non-profit in the sense that its purpose is to serve the members, not to make money. It needs money to provide services and benefits. In fact, it must be very careful to operate on a sound financial basis. But money is the means, not the end itself.</p>
	                    <div class="sec-title"><h4><b>SCCS ENCOURAGE SAVINGS</b></h4></div>
	                    <p>The first step in using money effectively is to accumulate savings for expected and unexpected demands. When something comes up, savings may cover the entire amount needed or may help the member secure financing. SCCs encourage members to save regularly, even if the amount they can set aside is small. Time and again it has been proven that even people with very little income are able to save something.</p>
						<p>SCCs use member’s savings to make loans to members who need capital for productive purposes. SCCs pay a fair return to savers for the use of their money. The rate paid should be based on SCC costs and the market savings rates and should not be well in excess of the market rate so as not to attract additional funds for which the SCC has no use.</p>
						<div class="sec-title"><h4><b>SCC LOANS</b></h4></div>
	                    <p>To protect member’s savings, SCC loans must be made on a sound basis, with very high expectations that borrowers will meet their obligations. Because the SCC mission is service, SCCs often make small loans that other organizations would dismiss as unprofitable. The SCC must be satisfied that a borrower will have enough income to make the payments and will use credit analysis to determine the borrower has the ability to repay as described in the loan contract.</p>
	                    <p>The loan interest rates charged at SCCs are generally affordable; they should cover SCC costs and compare to average loan rates available at other similar financial institutions. SCC loan rates increase or decrease according to the market.</p>
	                    <div class="sec-title"><h4><b>EDUCATION SUPPLEMENTS SERVICES</b></h4></div>
	                    <p>With a good understanding of financial matters, members can use their money more effectively. So education is another important part of the SCC tradition. SCCs help members understand why savings is important and how to use credit wisely. Seminars may address these or other special topics including improving wage-earning skills and credit rehabilitation.</p>
		</div>
