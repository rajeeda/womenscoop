	                    <div class="sec-title"><h4><b>1ST PRINCIPLE: VOLUNTARY AND OPEN MEMBERSHIP</b></h4></div>
	                    <p >Co-operatives are voluntary organisations, open to all persons able to use their services and willing to accept the responsibilities of membership without gender, social, racist, political or religious discrimination.</p>
	        		<div class="sec-title"><h4><b>2ND PRINCIPLE: DEMOCRATIC MEMBER CONTROL</b></h4></div>
	                    <p >Co-operatives are democratic organisations controlled by their members, who actively participate in setting their policies and making decisions. Men and women serving as elected representatives are accountable to the membership. In primary co-operatives, members have equal voting rights (one member, one vote) and co-operatives at other levels are also organised in a democratic manner.</p>
	                    <div class="sec-title"><h4><b>3RD PRINCIPLE: MEMBER ECONOMIC PARTICIPATION</b></h4></div>
	                    <p>Members contribute equitably to and democratically control the capital of their co-operative. At least part of that capital is usually the common property of the co-operative. Members usually receive limited compensation, if any, on capital subscribed as a condition of membership. Members allocate surplus for any or all of the following purposes:</p>
	                    <div class=" p_30">
	                    	<p class="font-14">Developing their co-operative possibly by setting up reserves, part of which at least would be indivisible.</br>
Benefitting members in proportion to their transactions with the co-operative.</br>
Supporting other activities approved by the membership.</p>
	                    </div>
	                    <div class="sec-title"><h4><b>4TH PRINCIPLE: AUTONOMY AND INDEPENDENCE</b></h4></div>
	                    <p>Co-operatives are autonomous, self-help organisations, controlled by their members. If they enter into agreements with other organisations, including governments or raise capital from external sources, they do so on terms that ensure democratic control by their members and maintain their co-operative autonomy.</p>
	                    <div class="sec-title"><h4><b>5TH PRINCIPLE: EDUCATION, TRAINING AND INFORMATION</b></h4></div>
	                    <p >Co-operatives provide education and training for their members, elected representatives, managers and employees so they can contribute effectively to the development of their co-operatives. They inform the general public, particularly young people and opinion leaders, about the nature and benefits of co-operatives.</p>
	                    <div class="sec-title"><h4><b>6TH PRINCIPLE: CO-OPERATION AMONG CO-OPERATIVES</b></h4></div>
	                    <p >Co-operatives serve their members most effectively and strengthen the co-operative movement by working together through local, national, regional and international structures.</p>
	                    <div class="sec-title"><h4><b>7TH PRINCIPLE: CONCERN FOR COMMUNITY</b></h4></div>
	                    <p >Co-operatives work for the sustainable development of their communities through policies approved by their members.</p>
