							<h3>HISTORY/INSTITUTIONAL BACKGROUND</h3>
							
						</div>
						<h4><b>Credit Unions in Asia </b></h4>
						<p>The savings and credit cooperative was introduced to Asian countries by the British colonial government as solution to the rural credit in India and Sri Lanka more than 100 years ago. Even before they had fully consolidated in Germany, cooperatives began spreading in some countries of Asia. In Thailand, cooperatives were introduced in 1915 and in the Philippines in 1892. </p>
						<p>Credit unions had a turning point in the 1960s when for the first time; the credit union training was conducted in Bangkok, Thailand by Social Economic Life of Asia (SELA). The conference was attended by young enthusiastic leaders across Asia who later on pioneered credit unions in their respective countries.  </p>
						<p>The Training cum seminar was very comprehensive and extensive covering those subjects as social and economic significance of credit unions, credit union philosophy and principles as well as credit union management. </p>
						<p>In collaboration with the Philippine Credit Union League (now Philippine Federation of Credit Cooperatives or PFCCO), CUNA (stands for Credit Union National Association in the USA) International organized the 1st training on Credit Union Development Course in Asia in 1961 in Baguio City, Philippines. The training brought together 25 delegates in 12 countries. As follow up of the training, credit unions principle philosophy and ideas were spread all over Asian countries resulting to establishment of first credit union in the Republic of China (Taiwan) in 1963, Hong Kong in 1964, Japan and Thailand in 1965, Malaysia and Vietnam in 1966, and Indonesia in 1970. Korea had already started the first credit union in 1960. Having established credit unions, the Philippine Credit Union League was established in 1960.  </p>
						<p>The same number of countries attended the second training organized with the Ministry of Agriculture and Cooperatives and Kasetsart University in October, 1965. The most attended was the third course conducted at Xavier University in Cagayan De Oro City in the Philippines in April 1969. It drew nearly 100 delegates from 15 countries in Asia including the World Council of Credit Unions (WOCCU) which was formed on the same year. Delegates realized the importance of having a regional body for credit unions to provide platform for sharing experience and learning among Asian credit unions. The training then concluded in the appointment of a Planning Committee mandated to plan for the establishment of the Asian Confederation of Credit Unions. </p>
						</br>
						<h4><b>Formation of ACCU</b></h4>
						<p>On April 28, 1971, delegates from 9 Asian countries - Japan, Hong Kong, Korea, Malaysia, the Philippines, Indonesia, Taiwan, Thailand, and South Vietnam came to Seoul, Korea to fulfill an individual collective dream - to cooperate with neighbors for the good of all. The representatives convened to assist and support the Planning Committee in developing the necessary requisites towards the formation of the confederation - an alliance of actual credit union centers leagues, federations of Asian countries with the mission to assist members to organize, expand, improve and integrate credit unions so they can fulfill their potential as effective instruments for the development of people in Asia. The Asian Confederation of Credit Unions or ACCU for short was officially formed. </p>
						<p>After the 4th Asian Regional Credit Union Training Seminar, the confederation held the Inauguration Meeting at the Cooperative Education Institute in Seoul. The meeting enacted the bylaws and approved the series of meetings of the Planning Committee.  On that momentous day, the national leagues in Asia transferred their membership from WOCCU to ACCU.  </p>
						<p>Having already formed their national leagues, five of the nine Asian countries Japan, Hong Kong, Korea, the Philippines, and Taiwan became the founding members of the confederation. The first year membership dues received was US$ 91 from the five founding members.    </p>
						<p>The Board Meeting of WOCCU held in Minneapolis, Minnesota, USA on May 19, 1971 approved the membership of ACCU.  The certificate of membership reads: "ACCU is accepted in the worldwide credit union movement and is a participant in all programs and provided by WOCCU Inc. services.”</p>
						<p>The affiliation entitled the members of ACCU to WOCCU services such as training programs, and technical support in feasibility studies, developing institutional growth development plans, and legal assistance that leads to the registration of national leagues and federations with their governments. The following year, Augustine J. R. Kang representing ACCU at WOCCU Board Meeting in Halifax, Canada, put the young ACCU into the world map of credit unions. </p>
						<p>Seconded by WOCCU, the Board of Directors appointed Augustine Kang, Jr. as the first General Manager of ACCU.  From 1971 to 1982, ACCU office had been based in Seoul with the office provided by the National Credit Union Federation of Korea. In 1983, the ACCU office was moved to Thailand and a new General Manager Mr. Somchit Supabanpot was appointed who served until 1994. He was replaced by the current Chief Executive Officer Ranjith Hettiarachchi.</p>
						<p>The Credit Union League of Thailand provided an office space for ACCU from 1983 to 1988. The confederation bought a five storey building in 1989 for its office and had used this building until May 2010. </p>
						</br>
						<h4><b>ACCU Today </b></h4>
						<p>In its role as a regional organization for credit unions, ACCU performs representation, development, liaison and coordination functions on behalf of the Asian credit union movements. It also provides members and potential members’ services in long and short-term technical assistance, training, and information.  Governed by a Board of Directors representing member organizations, ACCU operates through six Divisions: the Planning and Development, Member Services, Promotion, Marketing and Research, Business Development, and Support services.  </p>
						</br>
						<h4><b>Governance </b></h4>
						<p>ACCU operates as a regional representative organization of credit unions and similar cooperative financial institutions in the region of Asia. ACCU is representing 40.2 million individual members from more than 45,000 credit unions in 22 countries in Asia.  </p>
						<p>ACCU is owned by its member organizations. Member categories are:</p>
						<ul>
							<li>Regular – national service organization of credit unions/cooperatives with at least 20,000 individual members or one percent (1%) of the total population in which it is situated, whichever is less with voting right. </li>
							<li>Associate – national service organization of credit unions/cooperatives that have not reach the requirement for regular membership may be admitted without voting right. </li>
							<li>Affiliate - National Apex Cooperative Organizations, Cooperative Financial Institutions, Cooperatives Education and Research Institutions or the Credit Union Promotion Organizations or Patron Organizations of ACCU Any organization engaged in credit union promotion or cooperative promotion, training or support may be admitted as a non-voting affiliate.</li>
							<li>Supporter Members - Credit Unions, Credit Cooperatives, and Cooperative Banking Institutions and any organization engaged in credit union promotion or cooperative promotion, training or supporter may be admitted as a non-voting affiliate.</li>
						</ul>
						<p><b>General Meeting: </b>is composed of the official delegate from member organizations. The regular members have voting right and thus can be elected in the Board.
						</p>
						<p><b>Board of Directors:  </b>the General Meeting elects five (5) Directors from the voting regular members to oversee ACCU business. Those elected by the General Meeting shall elect among themselves: President, First Vice-President, Second Vice-President, Secretary, and Treasurer. The Chief Executive Officer shall act as an ex officio member without voting power. The Board of Directors shall be in their positions for two years. Each individual should not hold more than three consecutive terms.  
						</p>
