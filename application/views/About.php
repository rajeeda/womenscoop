						<h5><b>&nbsp;</b></h5>
						<h5><b>1971 to 1980 - Motivational</b></h5>
						<p>From 1971 to 1980, ACCU services were focused on introducing the credit union philosophy and principles to Asian countries. Annually, ACCU organized training to educate promoters of credit unions which led to the spread of credit unions in Asian countries. Except for the five founding members of ACCU (Japan, Hong Kong, Korea, Philippines and Taiwan), other federations that have become members of ACCU were formed during this period such as Bangladesh, Indonesia, Papua New Guinea, Thailand, Malaysia, Nepal, India and Sri Lanka.    </p>
						<p>The promotion of credit unions and building connections are an ongoing support for economies in transition (either from central economy or conflict) such as Cambodia, Lao PDR, Mongolia, Timor Leste, Pakistan and Vietnam.   </p>
						<h5><b>1981 to 1992 - Leadership Development </b></h5>
						<p>Aimed at building human capital, ACCU offered an intensive leadership course on credit union management and Field Organizers training during 1981 to 1992.  Many of those who were trained are still serving the credit union movement in their countries and internationally to some extent. Besides the ACCU sponsored training programs, cooperation among member organizations blossomed. Members of ACCU demonstrated their enthusiasm to learn from each other and share resources through exposure and internship programs between countries.  </p>
						<h5><b>1993 to 2005 - Professionalization </b></h5>
						<p>The decade is considered as the peak of business solutions development aimed to professionalize the credit union operation.  In 1998, ACCU implemented the project on Institutional Development of Credit Unions in Asia (INDECUA) that raised the level of awareness of leaders on the need to professionalize the credit union operation to meet the challenges of globalization. From this project, ACCU has evolved its role from merely technical assistance provider to business solutions developer.  All the solutions developed have been proven to be valuable to credit unions.  </p>
						<h5><b>2006 to 2014 - Quality Assurance </b></h5>
						<p>ACCU steps up to guarantee the value and differentiation of credit unions in the marketplace by developing systems of quality control. Tools such as ACCESS (stands for A1 Competitive Choice for Excellence in Service and Soundness) Branding, Risk Based Supervision, Stabilization Fund, Web-based Benchmarking Service and Governance Framework assist credit unions to raise its market performance and heighten its sense of mission. </p>
						<h5><b>2014 onwards – Integration of the Credit Union Networks </b></h5>
						<p>From 2014 ACCU takes leadership to integrate the credit union networks in Asia. It would mean the federations demonstrating leadership to ensure the viability, growth and sustainability of the network. The federations provide network support in areas of finance, liquidity management, human resource, lending services, marketing, and supervision.  </p>

